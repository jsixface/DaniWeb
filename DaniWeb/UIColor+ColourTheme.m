#import "UIColor+ColourTheme.h"

@implementation UIColor (ColourTheme)

+(UIColor *)DWMenuBackColour
{
    return  [UIColor colorWithRed:96.0/255 green:64.0/255 blue:128.0/255 alpha:1.0];
}

+(UIColor *)DWMenuLineColour
{
    return  [UIColor colorWithRed:180.0/255 green:110.0/255 blue:255.0/255 alpha:1.0];
}

+(UIColor *)DWMenuMaskColour
{
    return  [UIColor colorWithRed:60/255 green:40/255 blue:80/255 alpha:1.0];
}


@end
